package com.ruly.poker;

public enum Suit {
	SPADES(0), HEARTS(1), CLUBS(2), DIAMONDS(3);
	
	private final int value;

	private Suit(int value) {
		this.value = value;
	}
	public int getValue() {
		return this.value;
	}
	public static Suit getSuit(int value) {
		switch(value) {
			case 1: 
				return Suit.HEARTS;
			case 2: 
				return Suit.CLUBS;
			case 3: 
				return Suit.DIAMONDS;
			default:
				return Suit.SPADES;
		}
	}
	public String getLabel() {
		switch(value) {
			case 1: 
				return "Heart";
			case 2: 
				return "Club";
			case 3: 
				return "Diamond";
			default:
				return "Spade";
		}
	}
}
